import React from 'react';
import {
  cardRoot,
  cardHeader,
  cardTitle,
  cardDescription,
  cardContent,
  cardFooter,
} from './Card.css';

export const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={[cardRoot, className].filter(Boolean).join(' ')}
      {...props}
    />
  ),
);
Card.displayName = 'Card';

